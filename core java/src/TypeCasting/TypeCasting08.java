//	8.	wap to define a byte variable and assign its value to an int type variable and print both variables.
package TypeCasting;

public class TypeCasting08 {
    public static void main(String[] args) {
        byte b=23;
        int i=(int)b;
        System.out.println("byte value="+b);
        System.out.println("int value="+i);
    }
}
